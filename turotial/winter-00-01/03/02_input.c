#include <stdio.h>


int main() {

    // commnet
    /*
        conversion specifier:
            - %d - decimal - integer
            - %c - char
            - %s - string
            - %f - float
    */

    int a;

    printf("Enter a number: \n");
    scanf("%d", &a); 
    printf("a: %d", a);

    return 0;
}